export function checkTxtColor(txtColor) {
  let color = '#161616';

  switch (txtColor) {
    case '#FFFFFF':
      color = txtColor;
      break;
    case '#161616':
      color = txtColor;
      break;
    case '#161616':
      color = txtColor;
      break;
    default:
      break;
  }
  return color;
}
