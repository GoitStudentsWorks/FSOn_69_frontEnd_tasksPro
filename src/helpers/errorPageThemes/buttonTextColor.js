export function checkBtnTxtColor(btnColor) {
  let color = '';

  switch (btnColor) {
    case 'Dark':
      color = '#161616';
      break;
    case 'Light':
      color = '#161616';
      break;
    case 'Violet':
      color = '#FFFFFF';
      break;
    case 'Void':
      color = '#FFFFFF';
      break;
    default:
      break;
  }
  return color;
}
